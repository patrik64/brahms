    <div id="ambros" class="py-4 text-center text-2xl">
      August Wilhelm Ambros
    </div>
    <div class="py-2">*17.11.1816 Mauth bei Prag, †28.06.1876 Wien</div>
    <div>
      <div class="float-left p-2 flex-1 flex-col">
        <img
          class=" object-contain"
          src="/images/reviewers/ambros-august-wilhelm.jpg"
          alt="ambros"
        />
        <div class="text-xs">Ambros, 1862 &copy; Wikipedia</div>
      </div>
      <p class="text-justify">
        Musikhistoriker, Musikkritiker und Komponist. Neffe des Wiener
        Musikforschers Raphael Georg Kiesewetter, trotz juristischer Studien den
        Künsten zugetan. Unter dem Namen „Flamin“ Mitglied des von ihm
        mitgegründeten Prager „Davidsbundes“, dem auch Eduard Hanslick (als
        „Renatus“) angehörte. Sein musikalisches und journalistisches Vorbild
        fand Ambros in Robert Schumann, mit dem er von 1845 bis 1850 auch
        korrespondierte. Sein Werk Die Grenzen der Musik und Poesie. Eine Studie
        zur Ästhetik der Tonkunst (Prag 1856) entstand als Antwort auf Eduard
        Hanslicks Schrift Vom Musikalisch-Schönen, seinen Durchbruch als einer
        der bedeutendsten Musikgelehrten seiner Zeit hatte Ambros aber mit
        seiner (unvollendet gebliebenen) dreibändigen Geschichte der Musik
        (1862–1868). 1869 wurde er Professor für Musiktheorie an der Universität
        Prag, 1872 wechselte er als Kunst- und Musikgeschichtelehrer Erzherzog
        Rudolfs nach Wien, wo er auch am Konservatorium unterrichtete.
      </p>
    </div>
    <div class="py-2 text-justify">
      In seinen ästhetischen Urteilen stimmte er zu Beginn in vielen Dingen mit
      seinem Jugendfreund Hanslick überein, vertrat allerdings später auch
      andere Standpunkte. Er prägte seine Zeitgenossen v. a. durch zahlreiche
      Vorträge in Wien und auswärts, als Vorstand musikalischer Vereine und
      insbesondere als Musikkritiker und Redaktionsmitglied der Wiener Zeitung
      und der Wiener Abendpost sowie als Korrespondent auswärtiger Blätter.
    </div>
    <br />
    <br />
    <br />
    <div class="py-2 text-justify">
      Über seine Tätigkeit als Musikkritiker heißt es in dem Nachruf des <i
        >Neuen Wiener Abendblattes</i
      > vom 28. Juni 1876: „Seit seinem Aufenthalte in Wien, der mit Ende der sechziger
      Jahre begann, besorgte Ambros die musikalische Kiritk für die ‚Wiener Abendpost‘,
      die sich stets durch ein gewisses Wohlwollen für alle Produktion und Reproduktion
      auszeichnete.“
    </div>

