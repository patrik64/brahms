    <div id="frey" class="py-4 text-center text-2xl">Wilhelm Frey</div>
    <div class="py-2">*14.03.1828 Brünn, †02.08.1902 Waidhofen an der Ybbs/NÖ</div>
    <div>
      <div class="float-left p-2 flex-1 flex-col">
        <img class="object-contain" src="/images/reviewers/frey-wilhelm.jpg" alt="frey" />
        <div class="text-xs">&copy; Österreichs Illustrierte Zeitung</div>
        <div class="text-xs">Heft 30, 25. April 1909, S. 693.</div>
      </div>
      <p class="text-justify">Musikkritiker, Journalist und Schriftsteller, Vizepräsident des Journalisten- und Schriftstellervereines Concordia. Widmete sich zunächst der pädagogischen Laufbahn und wurde zum Professor für Mittelschulen graduiert. Zusätzlich absolvierte er zur Zeit seiner Lehrtätigkeit in Prag die Orgelschule. Während seiner Studien und seinem pädagogischen Beruf war Frey erfolgreich schriftstellerisch tätig und zählte in der zweiten Hälfte der 1850er- und ersten Hälfte der 1860er- Jahre zu den ersten deutschen Jugendschriftstellern neben den damaligen Größen Gustav Nieritz und Franz Hofmann. 1865 ging er nach Wien, wo er als Offizial in das administrative Revisionsamt der Kaiser Ferdinands-Nordbahn eintrat, dort Sekretär und Chef des Personalbureaus wurde, bevor er 1873 den Eisenbahndienst verließ und sich vollends der bis dahin nebenbei ausgeübten Journalistik widmete.</p>
      <p class="py-4 text-justify">1858-1867 Musikreferent der Morgenpost, seit der Gründung des Neuen Wiener Tagblatt (1873) „fast von den ersten Nummern an“ Musikkritiker, ab den 1890er Jahren Lokalschilderer dieses Blattes, dem er bis zu seinem Tod angehören sollte (Neues Wiener Tagblatt, 17. April 1909, S. 8). In diesem Nachruf heißt es weiters: „Seiner Liebe zum musikalisch Schönen, seiner Kenntnis des Theaters stand eine große Erfahrung zur Seite, an die Direktoren und Kapellmeister, Musiker und Sänger oft genug appellierten.“ Konkret wird Hofoperntheater-Direktor Jahn erwähnt, der „mit seinen Partituren oft genug bei Frey erschien, um dessen Gutachten einzuholen.“</p>
    </div>
    <br />
    <br />
    <div class="text-sm">Quelle: Elisabeth Th. Hilscher, Art. „Frey, Wilhelm‟, in: Oesterreichisches Musiklexikon online, Zugriff: 6.4.2021; Österreichs Illustrierte Zeitung, Heft 30, 25. April 1909, S. 694; Illustrierte Kronen-Zeitung, Nr. 3340, 17. April 1909, S. 8; Das Vaterland, 17. April 1909, S. 6; Arbeiter-Zeitung, 18. April 1909, S. 6; Neues Wiener Tagblatt, 17. April 1909, S. 8.</div>
