    <div id="gaigg" class="py-4 text-center text-2xl">Fritz Gaigg von Bergheim</div>
    <div class="py-2">*31.10.1848 Wien, †1918</div>
    <div class="py-2 text-justify">Musikschriftsteller, hauptberuflich Mittelschulprofessor an der Rainerschen Realschule in Wien, im Ersten Weltkrieg zusätzliche Lehrstelle an den Horakschen Musikschulen. Besonders eng ist sein Name als Musikkritiker mit der Reichspost verbunden. In einem Nachruf charakterisierte die Redaktion der Reichspost ihren verstorbenen Mitarbeiter mit den Worten: </div>
    <div class="py-2 text-justify"><i>„Als Musikschriftsteller war Friedrich Gaigg R. v. Bergheim in allen Wiener Theatern und Konzertsälen fast eine Generation hindurch zu Hause; die langjährige Erfahrung, die weit zurückreichende Personalkenntnis und seine ideale Liebe zur Musik, das waren die Charakteristiken dieses Mannes. in seinen Besprechungen spiegelte sich das wohlwollende Wesen des grundgütigen Menschen, der mit Vorliebe neu aufstrebende Talente förderte und sie mit freundlichen Worten in seinen niemals strengen Kritiken zum Aufstieg, aber auch zur Selbstzucht, zum fleißigen Studium ermunterte.“</i></div>
    <div class="py-2 text-justify">Kürzel: G. v. B.</div>
    <div class="text-sm">Quellen: Nachruf, in: Reichspost, Mittagsblatt, Jg. 25, Nr. 90, 25.02.1918, S. 3, Todesanzeige, in: Deutsches Volksblatt, Morgenausgabe, Jg. 30, Nr. 10469, 26.02.1918, S. 8. </div>
