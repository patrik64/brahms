    <div class="text-center text-4xl text-gray-600">Wiener Presse</div>

    <div class="py-4">
      Zeitschriften und Tageszeitungen wie auch Kunst- und Musikzeitschriften
      erwähnen und dokumentieren Aufführungen Brahms’scher Werke, anhand derer
      sich Erstaufführungen, Aufführungsdaten und -orte sowie Mitwirkende
      zurückverfolgen lassen. Umfangreiche Werkbesprechungen und Kritiken bieten
      zudem eine aufschlussreiche Informationsquelle, um Aufführungsprofile und
      die Werkrezeption durch die Zeitgenossen zu skizzieren. Auch bislang
      unbekannte biographische Einzelheiten zu Brahms’ Leben und (Nach-)Wirken
      kommen in Zeitungsmitteilungen zum Vorschein.
    </div>

    <div class="py-2">
      Doch dienten oft feuilletonistische, „unter dem Strich“ publizierte
      Konzert- und Werkbesprechungen nicht nur der Berichterstattung und der
      Mitteilung (musik-)ästhetischer Urteile. Der Brahms-Biograph Max Kalbeck
      bemerkte 1913, dass bereits in den 1880er Jahren Musik mit Politik
      vermengt worden sei. Der sozio-politische Kontext im Wien des späteren 19.
      Jahrhunderts war durch Spannungen zwischen verschiedenen Gruppen sowie
      durch tiefgreifende soziale Umwälzungen gekennzeichnet: Liberale,
      Sozialdemokraten, Christlich-soziale, Katholisch-konservative und diverse
      Deutsch-nationale Zirkel rangen um die Vorherrschaft. Alle diese Gruppen
      hatten ihre Sprachrohre und direkten oder indirekten Repräsentanten in der
      Presse. Je intensiver die Kontroversen und Franktionierung wurden, desto
      rhetorisch aufgeladener erschienen die Besprechungen und Kritiken in der
      Presse. In etlichen Rezensionen aus dieser Zeit und aus den
      unterschiedlichsten Zeitungen finden sich explizit oder durch Metaphern
      artikulierte sozio-politische Anschauungen ausgedrückt.
    </div>

    <div class="py-2">
      Es existierten Dutzende relevanter Blätter aus der Wiener
      Presselandschaft. In jüngerer Zeit rückte diese Fülle von Quellen in den
      Fokus von Forschenden, und zwar unter dem Blickwinkel der Politisierung
      innerhalb der Wiener Musikkritik, der starken Polarisierung und der
      Instrumentalisierung bestimmter Komponisten (vor allem Anton Bruckner,
      Richard Wagner und Johannes Brahms als wichtiger Künstlerpersönlichkeiten
      der Zeit) zum Zweck der Parteibildung (siehe die Unterseite „Literatur“).
      Finden sich zu einer Veranstaltung mehrere Kritiken, ist es oft
      erstaunlich, wie unterschiedlich die einzelnen Rezensenten ein Ereignis
      beurteilten bzw. wie diese ihre Leserschaft in eine bestimmte Richtung
      beeinflussen wollten. Gerade durch eine Gegenüberstellung jener
      gegensätzlichen Positionen erhöht sich die Wahrscheinlichkeit, abschätzen
      zu können, wie die unmittelbare Publikumsreaktion tatsächlich gewesen sein
      mag.
    </div>
