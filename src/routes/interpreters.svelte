    <!--brull-->
    <div id="brull" class="py-8 text-center text-3xl">Ignaz Brüll (1846-1907)</div>
    <div>
      <div class="float-right p-2 flex-1 flex-col">
        <img class="object-contain border-solid border border-black" src="/images/iterpreters/bruell-ignaz.jpg" alt="brull" />
        <div class="text-xs">In seinem Heim Liechtensteinstr. 4, 9. Bezirk, 1892 &copy; ÖNB / Grillich</div>
      </div>
      <p>Als Zehnjähriger wurde Ignaz Brüll Schüler des berühmten Pianisten Julius Epstein, sein Debüt gab er mit 13 und bereits zwei Jahre später wurde sein 1. Klavierkonzert uraufgeführt. Ab da war Ignaz Brüll ein bekannter Name im Musikgeschehen seiner Zeit. 1872 begann er als Lehrer an der renommierten Wiener Klavierschule Horak, deren Mitdirektor er 1881 wurde. In den 1890er Jahren wandelte sich sein Repertoire; ab diesem Zeitpunkt trat er fast ausschließlich als Interpret neuer Werke von Carl Goldmark, Johannes Brahms (Uraufführung der Klavierstücke op. 76, op. 116, Nr. 1-3, op. 117, Nr. 1 und 2 sowie op. 119, Nr. 2) sowie eigener Kompositionen in Erscheinung.</p>
      <p class="py-4">Doch nicht nur Brülls pianistische Qualitäten wurden geschätzt, sondern auch und vor allem seine menschlichen. So zählten etwa Carl Goldmark, Robert Fuchs, Eusebius Mandyczewski, Eduard Hanslick, Gustav Mahler, Theodor Billroth und nicht zuletzt Johannes Brahms zu seinem großen Freundeskreis. War er für Max Kalbeck der „Brahms-Pianist par Excellence“, so urteilte dieser gar über den liebevoll „Nazi“ titulierten Freund: „Wie etwas klingt, was ich gemacht habe, weiß ich erst, wenn ich's von Brüll gehört habe.“ (Quelle: <a rel="external" href="https://kulturstiftung.org/biographien/brull-ignaz-2" class="text-teal-400">Kulturstiftung der deutschen Vertriebenen</a>)</p>
    </div>

