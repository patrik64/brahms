    <!--barbi-->
    <div id="barbi" class="py-4 text-center text-3xl">Alice Barbi (1858-1948)</div>
    <div class="pt-4">Zunächst als Violinistin erfolgreich, begann Alice Barbi mit 20 Jahren eine Gesangsausbildung. Ihr Debüt als Sängerin gab sie am 2. April 1882, kurz darauf folgte ein Konzert in Rom vor der italienischen Königin unter der Leitung des Komponisten Giovanni Sgambati. Erfolgreiche Konzertreisen als königliche italienische Hofsängerin führten sie ab 1884 auch außerhalb Italiens, wobei sie in Wien erstmals 1889 auftrat. Hier etablierte sie sich rasch als Brahms-Sängerin par excellence.</div>
    <div class="pt-4">Krönung ihrer Wiener Karriere, welche dank ihres Engagements im Rahmen von Wohltätigkeitskonzerten auch nach ihrer Vermählung mit Baron Wolff-Stomersee nie gänzlich zum Erliegen kam, war mit Sicherheit ihr Abschiedskonzert am 21. Dezember 1893, bei dem Brahms sie am Klavier begleitete. Alice Barbi engagierte sich aber auch nach Brahms‘ Tod für dessen Musik, wovon zahlreiche Wohltätigkeitskonzerte (meist reine „Brahms-Abende“) genauso zeugen wie die Tatsache, dass sie die Erste war, die ihren Konzerterlös für die Errichtung eines Brahms-Denkmals spendete. Dieses Denkmal, das heute im Resselpark steht, ist also nicht zuletzt auch eine Erinnerung an die „große“ Alice Barbi und ihre Verehrung für Johannes Brahms.</div>
    <img class="m-auto border-solid border border-black mt-4" src="/images/iterpreters/barbi-alice.jpg" alt="barbi" />
