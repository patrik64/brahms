    <div class="text-center text-4xl text-gray-600">Brahms in Wien</div>

    <div class="py-4">
      Johannes Brahms (*7.5.1833 Hamburg, †3.4.1897 Wien) kam 1862 erstmals nach
      Wien und trat am 16. November als Pianist mit dem Quartett Hellmesberger
      vor das Wiener Publikum auf. Nach weiteren Konzerten in Wien, wo er mit
      prominenten Künstlerinnen und Künstlern der Wiener Szene musizierte und
      einige seiner Werke aufführte, fungierte er 1863-1864 als Chormeister der
      Wiener Singakademie. Am 1. Dezember 1867 fand in Wien die Uraufführung von
      drei Sätzen seines Deutschen Requiems statt. Dort wurden nicht nur viele
      seiner heute bekanntesten Werke uraufgeführt, zahlreiche bislang
      unbekannte Aufführungen fanden an verschiedenen Örtlichkeiten statt: in
      den Sälen Bösendorfer, Ehrbar, Streicher und des Musikvereins, im
      Volksgarten und in verschiedenen Vereinen. Seit 27. Dezember 1871
      dauerhaft in der Karlsgasse 4 des 4. Wiener Gemeindebezirks ansässig, war
      er von 1873 bis 1875 artistischer Direktor der Gesellschaft der
      Musikfreunde in Wien, zu deren Ehrenmitglied 1876 bzw. Direktionsmitglied
      1881 er gewählt wurde. Zunehmend etablierte sich Brahms als bedeutende
      Figur im Musikleben seiner Zeit; er war gefragtes Jurymitglied bei
      Wettbewerben, vertrat Österreich in Kommissionen und wurde in die
      Vorstände diverser Institutionen gewählt. Ein wesentlicher Faktor in der
      Brahms-Rezeption war letztlich auch seine Freundschaft zu zwei der
      einflussreichsten Musikkritikern seiner Zeit: Eduard Hanslick und Max
      Kalbeck.
