    <div class="py-4">
      Spätestens mit der Verleihung des „Österreichischen Ehrenzeichens für
      Kunst und Wissenschaft“ durch Kaiser Franz Joseph I. am 3. Mai 1896 wird
      Brahms' Relevanz als Künstler in der damaligen Österreichisch-Ungarischen
      Monarchie evident. Wie prominent Brahms tatsächlich war, zeigte sich aber
      auch nach seinem Tod durch das von der Stadt Wien bewilligte Ehrengrab am
      Zentralfriedhof und das von Rudolf Weyr gestaltete Denkmal, das 1908 im
      Resselpark am Karlsplatz enthüllt wurde. In Wiener Bibliotheken und
      Archiven wird letztlich ein erheblicher Teil der Brahms-relevanten Quellen
      verwahrt. In erster Linie zu nennen ist hier das <a
        rel="external"
